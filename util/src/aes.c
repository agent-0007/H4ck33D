/* rotation function */
static void AES_Rotate(uint8_t *in)
{
	uint8_t t = in[3];
	/* rotate */
	in[3] = in[2];
	in[2] = in[1];
	in[1] = in[0];
	in[0] = t;
}

/* multiply by two */
static uint8_t AES_GaloisMul2(uint8_t val)
{
	return val & 0x80 ? (val << 1) ^ 0x1b : (val << 1);
}

/* key schedule core */
static void AES_KeyScheduleCore(uint8_t *in, uint8_t iter)
{
	/* rotate */
	AES_Rotate(in);
	/* apply sbox operation on all four bytes */
	in[0] = sbox[in[0]];
	in[1] = sbox[in[1]];
	in[2] = sbox[in[2]];
	in[3] = sbox[in[3]];
	/* do the rcon operation on the first byte */
	in[3] ^= rcon[iter];
}

/* generate expanded key */
static void AES_KeySchedule(uint8_t *key, uint8_t *ekey)
{
	int b = 0, i, j;

	/* the first 16 bytes of expanded key are simply the key */
	memcpy(ekey + 0, key, 16);
	/* iteration value (rcon) is set to one */
	i = 1; b = 16;

	/* Until we have b bytes of expanded key, we do the following to generate n
	 * more bytes of expanded key */
	while (b != 176) {
		/* We do the following to create 4 bytes of expanded key */
		/* We create a 4-byte temporary variable  */
		uint8_t t[4];
		/* We assign the value of the previous four bytes in the expanded key
		 * to t */
		t[0] = ekey[(i - 1) * 16 + 0];
		t[1] = ekey[(i - 1) * 16 + 1];
		t[2] = ekey[(i - 1) * 16 + 2];
		t[3] = ekey[(i - 1) * 16 + 3];
		/* We perform the key schedule core (see above) on t, with i as the rcon
		 * iteration value */
		AES_KeyScheduleCore(t, i);
		/* We exclusive-OR t with the four-byte block n bytes before the new
		 * expanded key. This becomes the next 4 bytes in the expanded key */
		ekey[i * 16 + 12] = t[0] ^ ekey[(i - 1) * 16 + 12];
		ekey[i * 16 + 13] = t[1] ^ ekey[(i - 1) * 16 + 13];
		ekey[i * 16 + 14] = t[2] ^ ekey[(i - 1) * 16 + 14];
		ekey[i * 16 + 15] = t[3] ^ ekey[(i - 1) * 16 + 15];

		/* We then do the following three times to create the next twelve bytes
		 * of expanded key: */
		for (j = 2; j >= 0; j--) {
			/* We assign the value of the previous 4 bytes in the expanded key
			 * to t */
			t[0] = ekey[(i - 1) * 16 + j * 4 + 0];
			t[1] = ekey[(i - 1) * 16 + j * 4 + 1];
			t[2] = ekey[(i - 1) * 16 + j * 4 + 2];
			t[3] = ekey[(i - 1) * 16 + j * 4 + 3];
			/* We exclusive-OR t with the four-byte block n bytes before the new
			 * expanded key. This becomes the next 4 bytes in the expanded key
			 * */
			ekey[i * 16 + j * 4 + 0] = t[0] ^ ekey[i * 16 + (j + 1) * 4 + 0];
			ekey[i * 16 + j * 4 + 1] = t[1] ^ ekey[i * 16 + (j + 1) * 4 + 1];
			ekey[i * 16 + j * 4 + 2] = t[2] ^ ekey[i * 16 + (j + 1) * 4 + 2];
			ekey[i * 16 + j * 4 + 3] = t[3] ^ ekey[i * 16 + (j + 1) * 4 + 3];
		}
		/* we have generated 16 bytes */
		b += 16;
		/* increment i */
		i++;
	}
}

/* add round key step */
static void AES_AddRoundKey(uint8_t *key, uint8_t *state)
{
	int i;
	/* add round key */
	for (i = 0; i < 16; i++) {
		state[i] ^= key[i];
	}
}

/* bytes substitution */
static void AES_SubBytes(uint8_t *state)
{
	int i;
	/* add round key */
	for (i = 0; i < 16; i++) {
		state[i] = sbox[state[i]];
	}
}

/* shift rows */
static void AES_ShiftRows(uint8_t *state)
{
	uint8_t t[2];

	/* first four bytes are left uncahnged */

	/* 2nd row */
	t[0] = state[14];
    state[14] = state[10];
    state[10] = state[6];
    state[6] = state[2];
    state[2] = t[0];

	/* 3rd row */
	t[0] = state[13];
	t[1] = state[9];
	state[13] = state[5];
	state[9] = state[1];
	state[5] = t[0];
	state[1] = t[1];

	/* 4rd row */
	t[0] = state[0];
	state[0] = state[4];
	state[4] = state[8];
	state[8] = state[12];
	state[12] = t[0];

}

/* mix colums */
static void AES_MixColums(uint8_t *state)
{
	int i;
	uint8_t b[4], a[4], t[4];

	/* for every column */
	for (i = 0; i < 4; i++) {
		/* prepare mul by two */
		b[0] = AES_GaloisMul2(state[i * 4 + 0]);
		b[1] = AES_GaloisMul2(state[i * 4 + 1]);
		b[2] = AES_GaloisMul2(state[i * 4 + 2]);
		b[3] = AES_GaloisMul2(state[i * 4 + 3]);

		/* prepare mul by one */
		a[0] = state[i * 4 + 0];
		a[1] = state[i * 4 + 1];
		a[2] = state[i * 4 + 2];
		a[3] = state[i * 4 + 3];

		/* multiply by matrix */
		t[3] = a[0] ^ a[1] ^ b[2] ^ a[2] ^ b[3];
		t[2] = a[0] ^ b[1] ^ a[1] ^ b[2] ^ a[3];
		t[1] = b[0] ^ a[0] ^ b[1] ^ a[2] ^ a[3];
		t[0] = b[0] ^ a[1] ^ a[2] ^ b[3] ^ a[3];

		/* write back */
		state[i * 4 + 0] = t[0];
		state[i * 4 + 1] = t[1];
		state[i * 4 + 2] = t[2];
		state[i * 4 + 3] = t[3];
	}
}

/* perform encryption, all is kept in little endian here */
void AES_Encrypt(uint8_t *key, uint8_t *state)
{
	/* expanded key */
	uint8_t ex_key[176];
	uint8_t round;

	/* prepare key */
	AES_KeySchedule(key, ex_key);

	/* initial round  - add key */
	AES_AddRoundKey(ex_key, state);

	/* 9 main rounds */
	for (round = 1; round <= 9; round++) {
		/* sub bytes */
		AES_SubBytes(state);
		/* shift rows */
		AES_ShiftRows(state);
		/* mix columns */
		AES_MixColums(state);

		/* add round key */
		AES_AddRoundKey(ex_key + round * 16, state);
	}

	/* last round */
	AES_SubBytes(state);
	/* shift rows */
	AES_ShiftRows(state);
	/* add round key */
	AES_AddRoundKey(ex_key + 160, state);
}
